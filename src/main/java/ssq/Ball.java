package ssq;

public class Ball {
	private int index;
	private int value;
	public int getIndex() {
		return index;
	}
	public Ball(int index,int value){
		this.index=index;
		this.value=value;
	}
	public void setIndex(int index) {
		this.index = index;
	}
	public int getValue() {
		return value;
	}
	public void setValue(int value) {
		this.value = value;
	}
	
}
